export const getDashboardStats = (): DashboardStats => {
  const totalInventory = inventoryItems.reduce((sum, item) => sum + item.quantity, 0);
  const totalWarehouses = warehouses.filter(w => w.status === 'active').length;
  const activeDispatches = dispatchOrders.filter(o => ['locked', 'pending_approval', 'approved', 'in_transit'].includes(o.status)).length;
  const pendingApprovals = [
    ...dispatchOrders.flatMap(o => o.approvals.filter(a => a.status === 'pending')),
    ...replenishmentOrders.flatMap(o => o.approvals.filter(a => a.status === 'pending')),
  ].length;
  const activeAlerts = transportOrders.flatMap(t => t.alerts.filter(a => !a.resolved)).length;

  return {
    totalInventory,
    totalWarehouses,
    activeDispatches,
    pendingApprovals,
    activeAlerts,
    turnoverRate: generateTurnoverRate(),
    dispatchProgress,
    responseTime,
    recentActivities,
  };
};

export const getPendingApprovals = (): ApprovalRecord[] => {
  return [
    ...dispatchOrders.flatMap(o => o.approvals.filter(a => a.status === 'pending')),
    ...replenishmentOrders.flatMap(o => o.approvals.filter(a => a.status === 'pending')),
  ];
};
